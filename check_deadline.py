#==============================================
# –ò–º–ø–æ—Ä—Ç –±–∏–±–ª–∏–æ—Ç–µ–∫
#==============================================


import datetime


#==============================================
# –§—É–Ω–∫—Ü–∏—è –ø—Ä–∏–≤–µ–¥–µ–Ω–∏—è –¥–∞—Ç—ã –∫ —Ñ–æ—Ä–º–∞—Ç—É DD-MM-YYYY
#==============================================


# –ü–æ–ª—É—á–∞–µ–º –¥–∞—Ç—É –≤ –ª—é–±–æ–º —Ñ–æ—Ä–º–∞—Ç–µ
def normalize_date(date_str):
    # –ü—Ä–µ–æ–±—Ä–∞–∑—É–µ–º —ç—Ç—É –¥–∞—Ç—É –≤ –µ–¥–∏–Ω—ã–π —Ñ–æ—Ä–º–∞—Ç DD-MM-YYYY
    return date_str.replace(".", "-").replace("/", "-")


#==============================================
# –§—É–Ω–∫—Ü–∏—è –∏–Ω–∏—Ü–∏–∞–ª–∏–∑–∞—Ü–∏–∏ –¥–∞–Ω–Ω—ã—Ö
#==============================================


def data_init():

    # –ü–æ–ª—É—á–∞–µ–º –¥–∞–Ω–Ω—ã–µ –æ–± –∏–º–µ–Ω–∏ –∏ –æ–ø–∏—Å–∞–Ω–∏–∏ –∑–∞–º–µ—Ç–∫–∏
    username = input("üë§ –í–≤–µ–¥–∏—Ç–µ —Å–≤–æ—ë –∏–º—è >> ")
    content  = input("üìù –í–≤–µ–¥–∏—Ç–µ –æ–ø–∏—Å–∞–Ω–∏–µ –∑–∞–º–µ—Ç–∫–∏ >> ")

    while True:
        # –ü–æ–ª—É—á–∞–µ–º –¥–∞—Ç—É —Å–æ–∑–¥–∞–Ω–∏—è –∏ –¥–∞—Ç—É –¥–µ–¥–ª–∞–π–Ω–∞
        create_date_str = normalize_date(input("üóìÔ∏è  –í–≤–µ–¥–∏—Ç–µ –¥–∞—Ç—É —Å–æ–∑–¥–∞–Ω–∏—è –∑–∞–º–µ—Ç–∫–∏ (–¥–¥-–º–º-–≥–≥–≥–≥) >>"))
        issue_date_str  = normalize_date(input("üóìÔ∏è  –í–≤–µ–¥–∏—Ç–µ –¥–∞—Ç—É –¥–µ–¥–ª–∞–π–Ω–∞ –∑–∞–º–µ—Ç–∫–∏ (–¥–¥-–º–º-–≥–≥–≥–≥) >>"))

        # –ü—Ä–µ–æ–±—Ä–∞–∑—É–µ–º –¥–∞—Ç—ã –≤ –æ–±—ä–µ–∫—Ç—ã datetime
        create_date = datetime.datetime.strptime(create_date_str, "%d-%m-%Y")
        issue_date  = datetime.datetime.strptime(issue_date_str, "%d-%m-%Y")

        # –ï—Å–ª–∏ –¥–∞—Ç–∞ –¥–µ–¥–ª–∞–π–Ω–∞ –±–æ–ª—å—à–µ –¥–∞—Ç—ã —Å–æ–∑–¥–∞–Ω–∏—è, —Ç–æ –≤—ã—Ö–æ–¥–∏–º –∏–∑ —Ü–∏–∫–ª–∞
        if issue_date >= create_date:
            break
        else:
            print('‚ùå –î–∞—Ç–∞ –¥–µ–¥–ª–∞–π–Ω–∞ –Ω–µ –º–æ–∂–µ—Ç –±—ã—Ç—å –º–µ–Ω—å—à–µ –¥–∞—Ç—ã —Å–æ–∑–¥–∞–Ω–∏—è')
    
    # –í–æ–∑–≤—Ä–∞—â–∞–µ–º –¥–∞–Ω–Ω—ã–µ –≤ –≤–∏–¥–µ —Å–ø–∏—Å–∫–∞
    return [username, content, create_date, issue_date]


#==============================================
# –§—É–Ω–∫—Ü–∏—è –∏–Ω–∏—Ü–∏–∞–ª–∏–∑–∞—Ü–∏–∏ –∑–∞–≥–æ–ª–æ–≤–∫–æ–≤
#==============================================


def title_init():
    # –ò–Ω–∏—Ü–∏–∞–ª–∏–∑–∏—Ä—É–µ–º —Å–ø–∏—Å–æ–∫ –∑–∞–≥–æ–ª–æ–≤–∫–æ–≤ –∏ —Å—á—ë—Ç—á–∏–∫
    titles, count, title = [], 0, ' '

    while True:
        # –ü–æ–ª—É—á–∞–µ–º –∑–∞–≥–æ–ª–æ–≤–æ–∫
        title = str(input(f'\n–ù–∞–∑–≤–∞–Ω–∏–µ –∑–∞–≥–æ–ª–æ–≤–∫–∞ {count+1} - [Enter] –ø—Ä–æ–¥–æ–ª–∂–∏—Ç—å >> '))
        # –ï—Å–ª–∏ –∑–∞–≥–æ–ª–æ–≤–æ–∫ –Ω–µ –ø—É—Å—Ç, —Ç–æ –ø—Ä–æ–≤–µ—Ä—è–µ–º –µ–≥–æ –Ω–∞ —É–Ω–∏–∫–∞–ª—å–Ω–æ—Å—Ç—å
        if title != '':
            # –ï—Å–ª–∏ –∑–∞–≥–æ–ª–æ–≤–æ–∫ –Ω–µ —É–Ω–∏–∫–∞–ª—å–Ω—ã–π, —Ç–æ –≤—ã–≤–æ–¥–∏–º —Å–æ–æ–±—â–µ–Ω–∏–µ –æ–± –æ—à–∏–±–∫–µ
            if title.lower() not in list(map(str.lower, titles)):
                # –î–æ–±–∞–≤–ª—è–µ–º –∑–∞–≥–æ–ª–æ–≤–æ–∫ –≤ —Å–ø–∏—Å–æ–∫
                titles.append(title)
                count += 1
            else:
                print('\n‚ùå –¢–∞–∫–æ–π –∑–∞–≥–æ–ª–æ–≤–æ–∫ —É–∂–µ –µ—Å—Ç—å!')
        # –ï—Å–ª–∏ –∑–∞–≥–æ–ª–æ–≤–æ–∫ –ø—É—Å—Ç, —Ç–æ –≤—ã—Ö–æ–¥–∏–º –∏–∑ —Ü–∏–∫–ª–∞
        else:
            break

    # –í–æ–∑–≤—Ä–∞—â–∞–µ–º —Å–ø–∏—Å–æ–∫ –∑–∞–≥–æ–ª–æ–≤–∫–æ–≤
    return titles


#==============================================
# –§—É–Ω–∫—Ü–∏—è –∏–Ω–∏—Ü–∏–∞–ª–∏–∑–∞—Ü–∏–∏ —Å—Ç–∞—Ç—É—Å–æ–≤
#==============================================


def status_init():
    
    # –ò–Ω–∏—Ü–∏–∞–ª–∏–∑–∏—Ä—É–µ–º —Å–ª–æ–≤–∞—Ä—å —Å—Ç–∞—Ç—É—Å–æ–≤
    statuses = {
        '1': '‚úîÔ∏è  –í—ã–ø–æ–ª–Ω–µ–Ω–æ',
        '2': 'üü° –í –ø—Ä–æ—Ü–µ—Å—Å–µ',
        '3': '‚ùå –ù–µ –≤—ã–ø–æ–ª–Ω–µ–Ω–æ'
    }

    while True:
    # –í—ã–≤–æ–¥–∏–º —Å—Ç–∞—Ç—É—Å—ã –≤ –≤–∏–¥–µ —Ç–∞–±–ª–∏—Ü—ã
        for key, value in statuses.items():
            print(f'[{key}] - {value}')
    
        # –ü–æ–ª—É—á–∞–µ–º —Å—Ç–∞—Ç—É—Å –∑–∞–º–µ—Ç–∫–∏
        status = input("–í–≤–µ–¥–∏—Ç–µ —Å—Ç–∞—Ç—É—Å –∑–∞–º–µ—Ç–∫–∏ >> ")
        
        # –ï—Å–ª–∏ —Å—Ç–∞—Ç—É—Å –≤–∞–ª–∏–¥–Ω—ã–π, —Ç–æ –≤–æ–∑–≤—Ä–∞—â–∞–µ–º –µ–≥–æ
        if status in statuses:
            return statuses.get(status)
        # –ï—Å–ª–∏ —Å—Ç–∞—Ç—É—Å –Ω–µ –≤–∞–ª–∏–¥–Ω—ã–π, —Ç–æ –≤–æ–∑–≤—Ä–∞—â–∞–µ–º —Å–æ–æ–±—â–µ–Ω–∏–µ –æ–± –æ—à–∏–±–∫–µ
        else:
            print ('\n‚ùå –ù–µ–∏–∑–≤–µ—Å—Ç–Ω—ã–π —Å—Ç–∞—Ç—É—Å\n')


#==============================================
# –§—É–Ω–∫—Ü–∏—è –∏–Ω–∏—Ü–∏–∞–ª–∏–∑–∞—Ü–∏–∏ –¥–µ–¥–ª–∞–π–Ω–∞
#==============================================


def deadline_init(deadline_date, create_date):
    
    # –ü–æ–ª—É—á–∞–µ–º —Ä–∞–∑–Ω–∏—Ü—É –º–µ–∂–¥—É –¥–∞—Ç–∞–º–∏
    deadline = deadline_date - create_date

    # –í–æ–∑–≤—Ä–∞—â–∞–µ–º —Ä–∞–∑–Ω–∏—Ü—É –≤ –≤–∏–¥–µ —Å—Ç—Ä–æ–∫–∏
    if deadline.days <= 0:
        return '–î–µ–¥–ª–∞–π–Ω —É–∂–µ —Å–µ–≥–æ–¥–Ω—è!'
    elif deadline.days == 1:
        return '–î–µ–¥–ª–∞–π–Ω –∑–∞–≤—Ç—Ä–∞!'
    else:
        return '–î–µ–¥–ª–∞–π–Ω —á–µ—Ä–µ–∑ ' + str(deadline.days) + ' –¥–Ω–µ–π!'
     

#==============================================
# –°–±–æ—Ä–∫–∞ –∑–∞–º–µ—Ç–∫–∏
#==============================================


def build_note():
    
    # –ü–æ–ª—É—á–∞–µ–º –¥–∞–Ω–Ω—ã–µ
    data = data_init()
    data.extend([status_init(), title_init(), deadline_init(data[3], data[2])])

    # –§–æ—Ä–º–∏—Ä—É–µ–º —Å–ª–æ–≤–∞—Ä—å —Å –¥–∞–Ω–Ω—ã–º–∏
    note = {
        'üë§ –í–∞—à–µ –∏–º—è'          : data[0],
        'üìå –ó–∞–≥–æ–ª–æ–≤–∫–∏'         : data[5],
        'üìù –û–ø–∏—Å–∞–Ω–∏–µ –∑–∞–º–µ—Ç–∫–∏'  : data[1],
        'üìÖ –î–∞—Ç–∞ —Å–æ–∑–¥–∞–Ω–∏—è'     : data[2].strftime("%d-%m"),
        '‚è∞ –î–∞—Ç–∞ –¥–µ–¥–ª–∞–π–Ω–∞'     : data[3].strftime("%d-%m"),
        'üîî –°—Ç–∞—Ç—É—Å'            : data[4],
        '‚åõ –î–æ –¥–µ–¥–ª–∞–π–Ω–∞'       : data[6]
    }

    return note


#==============================================
# –§—É–Ω–∫—Ü–∏—è –≤—ã–≤–æ–¥–∞ –¥–∞–Ω–Ω—ã—Ö
#==============================================


def output(data):
    
    # –í—ã–≤–æ–¥–∏–º –∑–∞–≥–æ–ª–æ–≤–æ–∫
    print('='*50 + '\nüìù –í–∞—à–∞ –∑–∞–º–µ—Ç–∫–∞:\n' + '='*50)
    
    # –í—ã–≤–æ–¥–∏–º –¥–∞–Ω–Ω—ã–µ –≤ –≤–∏–¥–µ —Ç–∞–±–ª–∏—Ü—ã
    for key, value in data.items():
        # –ï—Å–ª–∏ –∑–Ω–∞—á–µ–Ω–∏–µ - —Å–ø–∏—Å–æ–∫, —Ç–æ –ø—Ä–∏–º–µ–Ω—è–µ–º –º–µ—Ç–æ–¥ join –¥–ª—è –≤—ã–≤–æ–¥–∞ –≤ –≤–∏–¥–µ —Å—Ç—Ä–æ–∫–∏
        if type(value) == list:
            print(f'{key}: {", ".join(value)}')
        # –ï—Å–ª–∏ –∑–Ω–∞—á–µ–Ω–∏–µ - —Å—Ç—Ä–æ–∫–∞, —Ç–æ –≤—ã–≤–æ–¥–∏–º –µ–≥–æ –≤ –≤–∏–¥–µ —Å—Ç—Ä–æ–∫–∏
        else:
            print(f'{key}: {value}')


#==============================================
# –ù–∞—á–∞–ª–æ –ø—Ä–æ–≥—Ä–∞–º–º—ã
#==============================================


def main():

    # –°–æ–∑–¥–∞—ë–º –∏ –≤—ã–≤–æ–¥–∏–º –∑–∞–º–µ—Ç–∫—É
    output(build_note())

if __name__ == '__main__':
    main()